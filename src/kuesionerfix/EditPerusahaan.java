    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        Connection connection = DBConnection.getConnection();
        Perusahaan perusahaan = new Perusahaan();
        perusahaan.setId(idPerusahaan);
        if (provinsiCombo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Provinsi harus diisi");
            return;
        }
        perusahaan.setKodeProvinsi(provinsiCombo.getSelectedItem().toString());
        if (kabupatenCombo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Kabupaten harus diisi");
            return;
        }
        perusahaan.setKodeKabupaten(kabupatenCombo.getSelectedItem().toString());
        if (kecamatanCombo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Kecamatan harus diisi");
            return;
        }
        perusahaan.setKodeKecamatan(kecamatanCombo.getSelectedItem().toString());
        if (kjuTF.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Kode KJU harus diisi");
            return;
        }
        perusahaan.setKodeKJU(kjuTF.getText());
        int noUrut = Integer.parseInt(urutTF.getText());
        if (noUrut == 0) {
            JOptionPane.showMessageDialog(this, "No Urut tidak boleh 0");
            return;
        }
        String query = "SELECT * FROM perusahaan WHERE no_urut = ?";
        try {
            PreparedStatement preparedStatement = connection.prepareStatement(query);
            preparedStatement.setInt(1, noUrut);
            ResultSet resultSet = preparedStatement.executeQuery();
            if (resultSet.next()) {
                JOptionPane.showMessageDialog(this, "No Urut sudah ada");
                return;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        perusahaan.setKodeUrut(noUrut);
        if (namaUsahaTF.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Nama usaha harus diisi");
            return;
        }
        perusahaan.setNamaPerusahaan(namaUsahaTF.getText());
        if (alamatTA.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Alamat harus diisi");
            return;
        }
        perusahaan.setAlamatPerusahaan(alamatTA.getText());
        if (phoneTF.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Telepon harus diisi");
            return;
        }
        perusahaan.setNoTelp(phoneTF.getText());
        if (faxTF.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Faksimili harus diisi");
            return;
        }
        perusahaan.setNoFax(faxTF.getText());
        if (bbhCombo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Bentuk Badan Hukum harus diisi");
            return;
        }
        perusahaan.setBentukBadanHukum(bbhCombo.getSelectedItem().toString());
        if (buttonGroupVisited.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Kunjungan harus diisi");
            return;
        }
        String visited = "";
        if (visitedRadio.isSelected()) {
            visited = "Sudah dikunjungi mendapatkan informasi dari pihak lain(dinas dll)";
        } else {
            visited = "Belum dikonfirmasi keberadaannya";
        }
        perusahaan.setVisited(visited);
        if (statusCombo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Status harus diisi");
            return;
        }
        perusahaan.setStatus(statusCombo.getSelectedItem().toString());
        if (buttonGroupTanamanPangan.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Tanaman Pangan harus diisi");
            return;
        }
        String tanamanPangan = "";
        if (adaPangan.isSelected()) {
            tanamanPangan = "Ada";
        } else {
            tanamanPangan = "Tidak Ada";
        }
        perusahaan.setTanamanPangan(tanamanPangan);
        if (buttonGroupHortikultura.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Hortikultura harus diisi");
            return;
        }
        String hortikultura = "";
        if (adaHortikultura.isSelected()) {
            hortikultura = "Ada";
        } else {
            hortikultura = "Tidak Ada";
        }
        perusahaan.setHortikultura(hortikultura);
        if (perkebunanCombo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Perkebunan harus diisi");
            return;
        }
        perusahaan.setPerkebunan(perkebunanCombo.getSelectedItem().toString());
        if (buttonGroupPeternakan.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Peternakan harus diisi");
            return;
        }
        String peternakan = "";
        if (adaPeternakan.isSelected()) {
            peternakan = "Ada";
        } else {
            peternakan = "Tidak Ada";
        }
        perusahaan.setPeternakan(peternakan);
        if (buttonGroupPerikanan.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Perikanan harus diisi");
            return;
        }
        String perikanan = "";
        if (adaPerikanan.isSelected()) {
            perikanan = "Ada";
        } else {
            perikanan = "Tidak Ada";
        }
        perusahaan.setPerikanan(perikanan);
        if (buttonGroupKehutanan.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Kehutanan harus diisi");
            return;
        }
        String kehutanan = "";
        if (adaKehutanan.isSelected()) {
            kehutanan = "Ada";
        } else {
            kehutanan = "Tidak Ada";
        }
        perusahaan.setKehutanan(kehutanan);
        if (jenisUsahaUtamaCombo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Jenis usaha utama harus diisi");
            return;
        }
        perusahaan.setJenisUsahaUtama(jenisUsahaUtamaCombo.getSelectedItem().toString());
        PerusahaanRepository perusahaanRepository = new PerusahaanRepository(connection);
        perusahaanRepository.update(perusahaan);
        this.dispose();
    }//GEN-LAST:event_editBtnActionPerformed

