    public void loadComboboxProv() {
        String sql = "SELECT * FROM provinsi";
        Connection connection = DBConnection.getConnection();
        try {
            Statement stmt = connection.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                provinsiCombo.addItem(rs.getString("nama"));
                provinsi.put(rs.getString("nama"), rs.getString("id"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    }

    public void loadComboboxKabupaten() {
        String kodeProv = provinsi.get(provinsiCombo.getSelectedItem());
        kabupatenCombo.removeAllItems();
        String sql = "SELECT * FROM kabupaten WHERE id_prov = " + kodeProv;
        Connection connection = DBConnection.getConnection();
        kabupatenCombo.addItem("-- Pilih Kabupaten --");
        try {
            Statement stmt = connection.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                kabupatenCombo.addItem(rs.getString("nama"));
                kabupaten.put(rs.getString("nama"), rs.getString("id_kab"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    }

    public void loadComboboxKecamatan() {
        String kodeKab = kabupaten.get(kabupatenCombo.getSelectedItem());
        kecamatanCombo.removeAllItems();
        String sql = "SELECT * FROM kecamatan WHERE id_kab = " + kodeKab;
        Connection connection = DBConnection.getConnection();
        kecamatanCombo.addItem("-- Pilih Kecamatan --");
        try {
            Statement stmt = connection.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                kecamatanCombo.addItem(rs.getString("nama"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    }

    public void loadPerusahaanEdit() {
        String sql = "SELECT * FROM perusahaan WHERE id = '" + idPerusahaan + "'";
        try {
            Connection connection = DBConnection.getConnection();
            Statement stmt = connection.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            if (rs.next()) {
                provinsiCombo.setSelectedItem(rs.getString("kode_prov"));
                kabupatenCombo.setSelectedItem(rs.getString("kode_kab"));
                kecamatanCombo.setSelectedItem(rs.getString("kode_kec"));
                kjuTF.setText(rs.getString("kode_kju"));
                urutTF.setText(rs.getString("no_urut"));
                namaUsahaTF.setText(rs.getString("nama_perusahaan"));
                alamatTA.setText(rs.getString("alamat_perusahaan"));
                phoneTF.setText(rs.getString("no_phone"));
                faxTF.setText(rs.getString("no_faksimili"));
                bbhCombo.setSelectedItem(rs.getString("bentuk_badan_hukum"));
                boolean visited = rs.getString("visited")
                        .equals("Sudah dikunjungi mendapatkan informasi dari pihak lain(dinas dll)");
                if (visited) {
                    visitedRadio.setSelected(true);
                } else {
                    notVisitedRadio.setSelected(true);
                }
                statusCombo.setSelectedItem(rs.getString("status"));
                boolean tanamanPangan = rs.getString("tanaman_pangan").equals("Ada") ? true : false;
                if (tanamanPangan) {
                    adaPangan.setSelected(true);
                } else {
                    tidakAdaPangan.setSelected(true);
                }
                boolean hortikultura = rs.getString("hortikultura").equals("Ada") ? true : false;
                if (hortikultura) {
                    adaHortikultura.setSelected(true);
                } else {
                    tidakAdaHortikultura.setSelected(true);
                }
                perkebunanCombo.setSelectedItem(rs.getString("perkebunan"));
                boolean perikanan = rs.getString("perikanan").equals("Ada") ? true : false;
                if (perikanan) {
                    adaPerikanan.setSelected(true);
                } else {
                    tidakAdaPerikanan.setSelected(true);
                }
                boolean peternakan = rs.getString("peternakan").equals("Ada") ? true : false;
                if (peternakan) {
                    adaPeternakan.setSelected(true);
                } else {
                    tidakAdaPeternakan.setSelected(true);
                }
                boolean kehutanan = rs.getString("kehutanan").equals("Ada") ? true : false;
                if (kehutanan) {
                    adaKehutanan.setSelected(true);
                } else {
                    tidakAdaKehutanan.setSelected(true);
                }
                jenisUsahaUtamaCombo.setSelectedItem(rs.getString("jenis_usaha_utama"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    }

