    private void setupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setupBtnActionPerformed
        // TODO add your handling code here:
        try {
            String host = hostInput.getText();
            int port = Integer.parseInt(portInput.getText());
            String database = databaseInput.getText();
            String uname = username.getText();
            String password = passwordFd.getText();
            
            DBConnection.setHost(host);
            DBConnection.setPort(port);
            DBConnection.setDatabase(database);
            DBConnection.setUsername(uname);
            DBConnection.setPassword(password);
            Login login = new Login();
            login.setVisible(true);
            login.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

        
    }//GEN-LAST:event_setupBtnActionPerformed

