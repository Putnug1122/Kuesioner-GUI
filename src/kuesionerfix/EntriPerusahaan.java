        public void loadComboboxProv() {
                String sql = "SELECT * FROM provinsi";
                Connection connection = DBConnection.getConnection();
                try {
                        Statement stmt = connection.createStatement();
                        ResultSet rs = stmt.executeQuery(sql);
                        while (rs.next()) {
                                provinsiCombo.addItem(rs.getString("nama"));
                                provinsi.put(rs.getString("nama"), rs.getString("id"));
                        }
                } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                }
        }

        public void loadComboboxKabupaten() {
                String kodeProv = provinsi.get(provinsiCombo.getSelectedItem());
                kabupatenCombo.removeAllItems();
                String sql = "SELECT * FROM kabupaten WHERE id_prov = " + kodeProv;
                Connection connection = DBConnection.getConnection();
                kabupatenCombo.addItem("-- Pilih Kabupaten --");
                try {
                        Statement stmt = connection.createStatement();
                        ResultSet rs = stmt.executeQuery(sql);
                        while (rs.next()) {
                                kabupatenCombo.addItem(rs.getString("nama"));
                                kabupaten.put(rs.getString("nama"), rs.getString("id_kab"));
                        }
                } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                }
        }

        public void loadComboboxKecamatan() {
                String kodeKab = kabupaten.get(kabupatenCombo.getSelectedItem());
                kecamatanCombo.removeAllItems();
                String sql = "SELECT * FROM kecamatan WHERE id_kab = " + kodeKab;
                Connection connection = DBConnection.getConnection();
                kecamatanCombo.addItem("-- Pilih Kecamatan --");
                try {
                        Statement stmt = connection.createStatement();
                        ResultSet rs = stmt.executeQuery(sql);
                        while (rs.next()) {
                                kecamatanCombo.addItem(rs.getString("nama"));
                        }
                } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                }
        }

